    private void equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalActionPerformed
       
        //we started the loop
        if (processingarea.getText().length()>0){
            
            //we defined the b value
            double b = Double.parseDouble(processingarea.getText());    
        
        //we started the switch-case    
        switch(sign){
            
            case "+":
            //we call the function
            add(toBeCalculatedValue, b);
            break;
            
            case "-":
            //we call the function
            substract(toBeCalculatedValue, b);
            break;
            
            case "*":
            //we call the function    
            multiply(toBeCalculatedValue, b);
            break;
            
            case "/":
            //we call the function
            divide(toBeCalculatedValue, b);
            break;
            
            case "%":
            //we call the function
            mod(toBeCalculatedValue, b);
            break;
           
            case "^":
            //we call the function
            xsqrty(toBeCalculatedValue, b);
            break;
        
        }
        }
    }//GEN-LAST:event_equalActionPerformed

