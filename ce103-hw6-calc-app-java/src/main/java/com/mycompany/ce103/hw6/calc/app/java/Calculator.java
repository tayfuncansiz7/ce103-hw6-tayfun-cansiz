    @Override
    public void add(double a, double b){
        //has been returned
        setResult(a+b);
     }

    @Override
    public void substract(double a, double b) {
        //has been returned
        setResult(a-b);
    }

    @Override
    public void multiply(double a, double b) {
        //has been returned
        setResult(a*b);
    }

    @Override
    public void divide(double a, double b) {
        //has been returned
        setResult(a/b);
    }
    
    @Override
    public void mod(double a, double b){
        //has been returned
        setResult(a%b);
    }
    
    @Override
    public void xsqrty(double a, double b){
        //has been returned
        setResult(Math.pow(a, b));
    }
       
    private void setResult(double result){
    
        //we printed result on the screen
        processingarea.setText(Double.toString(result));
        //we printed blank on the screen
        resultarea.setText("");
        
    }
    
    private void storeToBeCalculatedValue(String sign){
    
        //we defined the value 
        String value = processingarea.getText();
        //we defined the toBeCalculatedValue value
        toBeCalculatedValue = Double.parseDouble(value);
    
        //we printed value+blank+sign on the screen
        resultarea.setText(value + "" + sign);
        //we printed blank on the screen
        processingarea.setText("");
    
    }
 
